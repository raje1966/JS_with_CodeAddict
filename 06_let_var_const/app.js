//let Vs Const Vs Var

//using var - wherein values can be changed.

var value = "some value";
value = "some other value";

//using let - wherein values can be changed.

let name = "raj";
name = "bhavana";

//using const (constant) - can't re-assign
const lastName = "jordan";

console.log(value);
console.log(name);
console.log(lastName);